#include "../pipex.h"

void	close_fds(int argc, int fd[MAX_FD][2])
{
	int	k;
	
	k = 0;
	while (k < (argc - 4))
	{
		close(fd[k][0]);
		close(fd[k][1]);
		k++;
	}
}
